namespace QueueStack
{
    public class Matrix
    {
        public int[][] UpdateMatrix(int[][] mat)
        {
            if (mat.Length == 0)
            {
                return new int[0][];
            }
            Queue<int[]> queue = new Queue<int[]>();



            for (int i = 0; i < mat.Length; i++)
            {
                for (int j = 0; j < mat[i].Length; j++)
                {
                    if (mat[i][j] == 0)
                    {
                        stack.Push(new int[] { i, j });

                    }
                    else
                    {
                        mat[i][j] = int.MaxValue;


                    }
                }
            }
            
            return mat;
        }

    }



